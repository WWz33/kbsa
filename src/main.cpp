UnitigOptions parse_unitig_args(int argc, char** argv)
{
  UnitigOptions opt;
  for (int i = 2; i < argc; ++i) {
    if (strcmp(argv[i], "--bulk1") == 0 && i+1 < argc) opt.bulk1_db = argv[++i];
    else if (strcmp(argv[i], "--bulk2") == 0 && i+1 < argc) opt.bulk2_db = argv[++i];
    else if (strcmp(argv[i], "--bulk1-hist") == 0 && i+1 < argc) opt.bulk1_hist = argv[++i];
    else if (strcmp(argv[i], "--bulk2-hist") == 0 && i+1 < argc) opt.bulk2_hist = argv[++i];
    else if ((strcmp(argv[i], "-o") == 0 || strcmp(argv[i], "--output") == 0) && i+1 < argc)
      opt.output = argv[++i];
    else if (strcmp(argv[i], "--min-count") == 0 && i+1 < argc) opt.min_count = std::atoi(argv[++i]);
    else if (strcmp(argv[i], "--max-opp") == 0 && i+1 < argc) opt.max_opp = std::atoi(argv[++i]);
    else if (strcmp(argv[i], "--min-unitig") == 0 && i+1 < argc) opt.min_unitig = std::atoi(argv[++i]);
    else if (strcmp(argv[i], "--tau") == 0 && i+1 < argc) opt.tau = std::atof(argv[++i]);
    else if (strcmp(argv[i], "--error-rate") == 0 && i+1 < argc) opt.error_rate = std::atof(argv[++i]);
    else if (strcmp(argv[i], "--map-ref") == 0 && i+1 < argc) opt.map_ref = argv[++i];
    else if (strcmp(argv[i], "--parent1") == 0 && i+1 < argc) opt.parent1_db = argv[++i];
    else if (strcmp(argv[i], "--parent2") == 0 && i+1 < argc) opt.parent2_db = argv[++i];
    else if (strcmp(argv[i], "--extend") == 0) opt.extend = true;
    else {
      fprintf(stderr, "Unknown option: %s\n", argv[i]);
      print_unitig_usage(argv[0]);
      exit(1);
    }
  }
  return opt;
}

int cmd_unitig(int argc, char** argv)
{
  if (argc < 3) { print_unitig_usage(argv[0]); return 1; }
  auto opt = parse_unitig_args(argc, argv);

  if (opt.bulk1_db.empty() || opt.bulk2_db.empty() || opt.output.empty()) {
    fprintf(stderr, "[unitig] Error: --bulk1, --bulk2, -o required\n");
    print_unitig_usage(argv[0]);
    return 1;
  }

  Params params;
  params.bulk1_to_bulk2_scale = 1.0;
  params.error_rate = opt.error_rate;

  auto hist1 = opt.bulk1_hist.empty()
    ? compute_histogram_from_db(opt.bulk1_db)
    : load_histogram(opt.bulk1_hist);
  auto hist2 = opt.bulk2_hist.empty()
    ? compute_histogram_from_db(opt.bulk2_db)
    : load_histogram(opt.bulk2_hist);
  uint64_t peak1 = find_peak_depth(hist1);
  uint64_t peak2 = find_peak_depth(hist2);
  params.bulk1_to_bulk2_scale = static_cast<double>(peak2) / std::max<uint64_t>(peak1, 1);

  fprintf(stderr, "[unitig] bulk1 peak=%lu, bulk2 peak=%lu, scale=%.3f\n",
          peak1, peak2, params.bulk1_to_bulk2_scale);

  // --- BCALM2 pipeline: stream diff k-mers → FASTA → bcalm2 → read unitigs ---
  std::string tmp_dir = opt.output + "_tmp";
  fs::create_directories(tmp_dir);
  std::string diff_fa = tmp_dir + "/diff_kmers.fa";

  fprintf(stderr, "[unitig] Streaming differential k-mers to FASTA...\n");
  auto diff_stats = stream_differential_kmers_to_fasta(
      opt.bulk1_db, opt.bulk2_db, hist1, hist2,
      diff_fa, opt.min_count, opt.max_opp,
      opt.parent1_db, opt.parent2_db);
  uint32_t k = diff_stats.k;
  fprintf(stderr, "[unitig] k=%u, differential k-mers=%lu (enriched_min=%u, opposite_max=%u)\n",
          k, diff_stats.total_diff_kmers, diff_stats.enriched_min, diff_stats.opposite_max);

  if (diff_stats.total_diff_kmers == 0) {
    fprintf(stderr, "[unitig] Warning: no differential k-mers found.\n");
    fs::remove_all(tmp_dir);
    return 0;
  }

  // Run BCALM2
  std::string bcalm_prefix = tmp_dir + "/bcalm_out";
  std::string bcalm_cmd = "bcalm -in " + diff_fa
      + " -kmer-size " + std::to_string(k)
      + " -abundance-min 1"
      + " -nb-cores 4"
      + " -out " + bcalm_prefix
      + " > /dev/null 2>&1";
  fprintf(stderr, "[unitig] Running BCALM2...\n");
  int ret = std::system(bcalm_cmd.c_str());
  if (ret != 0) {
    fprintf(stderr, "[unitig] Error: BCALM2 failed (exit %d)\n", ret);
    fs::remove_all(tmp_dir);
    return 1;
  }

  // Read BCALM2 unitigs
  std::string bcalm_unitigs_fa = bcalm_prefix + ".unitigs.fa";
  auto unitigs = read_bcalm2_unitigs(bcalm_unitigs_fa, opt.min_unitig);
  fprintf(stderr, "[unitig] BCALM2 produced %zu unitigs (min_length=%u)\n",
          unitigs.size(), opt.min_unitig);

  // Clean up temp files
  fs::remove_all(tmp_dir);

  if (opt.extend && !unitigs.empty()) {
    uint32_t ext_min = std::max<uint32_t>(3, static_cast<uint32_t>(
        std::max(peak1, peak2) / 5));
    fprintf(stderr, "[unitig] Two-phase extension (min_ext_count=%u)...\n", ext_min);
    unitigs = extend_unitigs_two_phase(unitigs, k, opt.bulk1_db, opt.bulk2_db, ext_min);
    fprintf(stderr, "[unitig] Extension complete, %zu unitigs\n", unitigs.size());
  }

  // Score unitigs using KMC random access (no KmerMap needed)
  fprintf(stderr, "[unitig] Scoring unitigs (tau=%.1f)...\n", opt.tau);
  KmcRandomAccess db1_ra, db2_ra;
  if (!db1_ra.open(opt.bulk1_db))
    throw std::runtime_error("Cannot open KMC RA: " + opt.bulk1_db);
  if (!db2_ra.open(opt.bulk2_db))
    throw std::runtime_error("Cannot open KMC RA: " + opt.bulk2_db);

  auto scores = score_and_rank_unitigs_kmc(
      unitigs, k, db1_ra, db2_ra, params, static_cast<float>(opt.tau));
  db1_ra.close();
  db2_ra.close();

  // Write FASTA
  std::string fa_path = opt.output + "_unitigs.fa";
  std::ofstream fa_out(fa_path);
  if (!fa_out) {
    fprintf(stderr, "[unitig] Error: cannot open %s for writing\n", fa_path.c_str());
    return 1;
  }
  for (size_t i = 0; i < scores.size(); ++i) {
    fa_out << ">" << scores[i].id
           << " len=" << scores[i].length
           << " rank_score=" << scores[i].rank_score << "\n";
    fa_out << scores[i].sequence << "\n";
  }
  fa_out.close();

  // Write TSV
  std::string tsv_path = opt.output + "_unitigs.tsv";
  std::ofstream tsv_out(tsv_path);
  if (!tsv_out) {
    fprintf(stderr, "[unitig] Error: cannot open %s for writing\n", tsv_path.c_str());
    return 1;
  }
  tsv_out << "unitig_id\tlength\tn_kmers\tdominant_dir\tcoherence\tmean_z\t"
             "excess_per_kb\tsum_excess_mass\tsum_bulk1\tsum_bulk2\n";
  for (const auto& s : scores) {
    const char* dir = (s.dominant_dir > 0) ? "BULK1" : "BULK2";
    char buf[512];
    int n = snprintf(buf, sizeof(buf),
      "%s\t%u\t%u\t%s\t%.4f\t%.4f\t%.4f\t%.4f\t%lu\t%lu\n",
      s.id.c_str(), s.length, s.n_kmers, dir,
      s.coherence, s.mean_z, s.excess_per_kb, s.sum_excess_mass,
      s.sum_bulk1, s.sum_bulk2);
    tsv_out.write(buf, n);
  }
  tsv_out.close();

  fprintf(stderr, "[unitig] Output: %s (%zu unitigs)\n", fa_path.c_str(), scores.size());
  fprintf(stderr, "[unitig] Output: %s\n", tsv_path.c_str());

  // Optional reference mapping via minimap2 → BED output
  if (!opt.map_ref.empty()) {
    fprintf(stderr, "[unitig] Mapping unitigs to reference via minimap2: %s\n",
            opt.map_ref.c_str());

    // Score → rank_score map for BED scoring
    std::unordered_map<std::string, std::pair<float, char>> id_to_score;
    for (const auto& s : scores)
      id_to_score[s.id] = {s.rank_score, s.dominant_dir > 0 ? '+' : '-'};

    std::string paf_path = opt.output + "_unitigs.paf";
    std::string bed_path = opt.output + "_unitigs.bed";

    // minimap2 -cx asm5: precise asm-to-ref, -N 50 keep up to 50 secondary
    // -p 0.5: report secondary if score >= 50% of primary (paralog tolerance)
    std::string cmd = "minimap2 -cx asm5 -N 50 -p 0.5 -t 4 \""
        + opt.map_ref + "\" \"" + fa_path + "\" > \""
        + paf_path + "\" 2>/dev/null";
    int ret = std::system(cmd.c_str());
    if (ret != 0) {
      fprintf(stderr, "[unitig] minimap2 failed (exit %d). Skipping mapping.\n", ret);
    } else {
      // Parse PAF → BED. PAF columns:
      // qname qlen qstart qend strand tname tlen tstart tend matches alen mapq ...
      std::ifstream paf(paf_path);
      std::ofstream bed(bed_path);
      if (!paf || !bed) {
        fprintf(stderr, "[unitig] Error: cannot open PAF/BED files\n");
        return 1;
      }

      // For each query, keep only highest-score alignment(s)
      std::unordered_map<std::string, std::vector<std::string>> q_lines;
      std::string line;
      uint32_t n_lines = 0;
      while (std::getline(paf, line)) {
        if (line.empty()) continue;
        size_t tab = line.find('\t');
        if (tab == std::string::npos) continue;
        std::string qname = line.substr(0, tab);
        q_lines[qname].push_back(line);
        ++n_lines;
      }
      paf.close();

      uint32_t n_mapped = 0;
      for (auto& [qname, lines] : q_lines) {
        auto sit = id_to_score.find(qname);
        if (sit == id_to_score.end()) continue;
        for (const auto& ln : lines) {
          // Parse PAF columns
          std::vector<std::string> cols;
          size_t pos = 0, tab;
          while ((tab = ln.find('\t', pos)) != std::string::npos) {
            cols.push_back(ln.substr(pos, tab - pos));
            pos = tab + 1;
          }
          cols.push_back(ln.substr(pos));
          if (cols.size() < 12) continue;
          const std::string& tname = cols[5];
          uint64_t tstart = std::stoull(cols[7]);
          uint64_t tend = std::stoull(cols[8]);
          char strand = cols[4][0];
          char buf[512];
          int n = snprintf(buf, sizeof(buf), "%s\t%lu\t%lu\t%s\t%.1f\t%c\n",
              tname.c_str(), tstart, tend, qname.c_str(),
              static_cast<double>(sit->second.first), strand);
          bed.write(buf, n);
          ++n_mapped;
        }
      }
      bed.close();
      fprintf(stderr, "[unitig] Mapped %u alignments from %zu unitigs → %s\n",
              n_mapped, q_lines.size(), bed_path.c_str());
      // Keep .paf for inspection; user can delete
    }
  }

  return 0;
}

int main(int argc, char** argv)
{
  if (argc < 2) {
    print_usage(argv[0]);
    return 1;
  }

  if (strcmp(argv[1], "score") == 0)
    return cmd_score(argc, argv);
  if (strcmp(argv[1], "count") == 0)
    return cmd_count(argc, argv);
  if (strcmp(argv[1], "anchor") == 0)
    return cmd_anchor(argc, argv);
  if (strcmp(argv[1], "rank") == 0)
    return cmd_rank(argc, argv);
  if (strcmp(argv[1], "unitig") == 0)
    return cmd_unitig(argc, argv);

  fprintf(stderr, "Unknown command: %s\n", argv[1]);
  print_usage(argv[0]);
  return 1;
}
