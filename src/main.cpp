// Split z-score into directional excess-mass.
// score_kmer: z > 0 = BULK1-enriched (case), z < 0 = BULK2-enriched (ctrl).
// Returns {bulk1_excess, bulk2_excess} after subtracting tau.
static inline std::pair<double, double> directional_excess(double z, double tau)
{
  if (z > 0.0) {
    double e = z - tau;
    return {e > 0.0 ? e : 0.0, 0.0};
  }
  double e = -z - tau;
  return {0.0, e > 0.0 ? e : 0.0};
}

static void apply_permutation_fdr(
    std::vector<Peak>& all_peaks,
    const std::vector<ChromResult>& results,
    const std::vector<std::pair<std::string, std::string>>& chroms,
    uint32_t n_perm, uint32_t W, uint32_t B, int threads)
{
  fprintf(stderr, "[kbsa anchor] Permutation FDR: %u perms, block=%u bp\n", n_perm, B);
  auto t_perm0 = std::chrono::steady_clock::now();

  std::vector<double> obs_score(all_peaks.size());
  for (size_t i = 0; i < all_peaks.size(); ++i)
    obs_score[i] = std::max(all_peaks[i].pos_ex, all_peaks[i].neg_ex);

  std::vector<uint64_t> ge_count(all_peaks.size(), 0);

  std::vector<std::vector<size_t>> chrom_peaks(chroms.size());
  std::unordered_map<std::string, size_t> chrom_idx;
  for (size_t ci = 0; ci < chroms.size(); ++ci)
    chrom_idx[chroms[ci].first] = ci;
  for (size_t i = 0; i < all_peaks.size(); ++i) {
    auto it = chrom_idx.find(all_peaks[i].chrom);
    if (it != chrom_idx.end())
      chrom_peaks[it->second].push_back(i);
  }

  const uint32_t blocks_per_win = W / B;

  #pragma omp parallel num_threads(threads)
  {
    std::mt19937_64 rng(42 + omp_get_thread_num());
    std::vector<Block> shuffled;

    #pragma omp for schedule(dynamic)
    for (uint32_t perm = 0; perm < n_perm; ++perm) {
      for (size_t ci = 0; ci < chroms.size(); ++ci) {
        const auto& src_blocks = results[ci].blocks;
        if (src_blocks.empty()) continue;
        shuffled = src_blocks;
        std::shuffle(shuffled.begin(), shuffled.end(), rng);

        for (size_t pi : chrom_peaks[ci]) {
          uint64_t mb = all_peaks[pi].mb;
          size_t bi_start = mb * blocks_per_win;
          size_t bi_end = std::min(bi_start + blocks_per_win, shuffled.size());
          double null_pos = 0, null_neg = 0;
          for (size_t bi = bi_start; bi < bi_end; ++bi) {
            null_pos += shuffled[bi].pos_ex;
            null_neg += shuffled[bi].neg_ex;
          }
          double null_score = std::max(null_pos, null_neg);
          if (null_score >= obs_score[pi]) {
            #pragma omp atomic
            ge_count[pi] += 1;
          }
        }
      }
    }
  }

  for (size_t i = 0; i < all_peaks.size(); ++i)
    all_peaks[i].p_emp = (1.0 + ge_count[i]) / (1.0 + n_perm);

  std::vector<size_t> order(all_peaks.size());
  for (size_t i = 0; i < order.size(); ++i) order[i] = i;
  std::sort(order.begin(), order.end(),
    [&](size_t a, size_t b) { return all_peaks[a].p_emp < all_peaks[b].p_emp; });
  double m = (double)all_peaks.size();
  double prev_q = 1.0;
  for (ssize_t r = (ssize_t)order.size() - 1; r >= 0; --r) {
    size_t i = order[r];
    double q = all_peaks[i].p_emp * m / (r + 1);
    if (q > 1.0) q = 1.0;
    if (q < prev_q) prev_q = q;
    all_peaks[i].q_bh = prev_q;
  }

  auto dt_perm = std::chrono::duration_cast<std::chrono::seconds>(
    std::chrono::steady_clock::now() - t_perm0).count();
  fprintf(stderr, "[kbsa anchor] Permutation FDR done in %lds.\n", (long)dt_perm);
}

static int write_peak_bed(
    const std::string& path,
    const std::vector<Peak>& all_peaks,
    uint32_t W, bool use_mean, bool with_pvals)
{
  FILE* out = std::fopen(path.c_str(), "wb");
  if (!out) {
    fprintf(stderr, "ERROR: cannot open output: %s\n", path.c_str());
    return 1;
  }
  if (with_pvals)
    std::fputs("#chrom\tstart\tend\tn_kmers\tpos_excess\tneg_excess\tdir\tcoherence\tscore\tp_emp\tq_bh\n", out);
  else
    std::fputs("#chrom\tstart\tend\tn_kmers\tpos_excess\tneg_excess\tdir\tcoherence\tscore\n", out);

  char buf[kBedRowBufSize];
  for (const auto& p : all_peaks) {
    double score = std::max(p.pos_ex, p.neg_ex);
    double total_ex = p.pos_ex + p.neg_ex;
    double coherence = total_ex > 0.0 ? score / total_ex : 0.0;
    const char* dir = (p.pos_ex >= p.neg_ex) ? "BULK1" : "BULK2";
    double out_score = use_mean ? (p.n > 0 ? score / p.n : 0.0) : score;
    uint64_t start = p.mb * W;
    uint64_t end = start + W;
    int n;
    if (with_pvals) {
      n = snprintf(buf, sizeof(buf), "%s\t%lu\t%lu\t%lu\t%.4f\t%.4f\t%s\t%.4f\t%.4f\t%.4g\t%.4g\n",
        p.chrom.c_str(), (unsigned long)start, (unsigned long)end,
        (unsigned long)p.n, p.pos_ex, p.neg_ex, dir, coherence, out_score, p.p_emp, p.q_bh);
    } else {
      n = snprintf(buf, sizeof(buf), "%s\t%lu\t%lu\t%lu\t%.4f\t%.4f\t%s\t%.4f\t%.4f\n",
        p.chrom.c_str(), (unsigned long)start, (unsigned long)end,
        (unsigned long)p.n, p.pos_ex, p.neg_ex, dir, coherence, out_score);
    }
    std::fwrite(buf, 1, n, out);
  }
  std::fclose(out);
  return 0;
}

// Per-chromosome parallel scan: query each ref k-mer against both KMC DBs,
// accumulate directional excess into windows + blocks. Each thread opens its
// own KMC RA index (~1.5GB each).
static void run_peak_scan(
    const std::vector<std::pair<std::string, std::string>>& chroms,
    const std::string& bulk1_db,
    const std::string& bulk2_db,
    const Params& params,
    uint32_t k, uint32_t W, uint32_t B, double tau,
    uint64_t min_total, uint64_t max_total,
    uint32_t n_perm, int threads,
    std::vector<ChromResult>& results)
{
  #pragma omp parallel num_threads(threads)
  {
    KmcRandomAccess t_bulk1_ra, t_bulk2_ra;
    t_bulk1_ra.open(bulk1_db);
    t_bulk2_ra.open(bulk2_db);

    std::vector<char> t_fwd(k + 1, '\0');
    std::vector<char> t_rc(k + 1, '\0');
    std::vector<char> t_canon(k + 1, '\0');

    #pragma omp for schedule(dynamic)
    for (size_t ci = 0; ci < chroms.size(); ++ci) {
      const auto& [cname, cseq] = chroms[ci];
      const uint64_t n_pos = cseq.size() - k + 1;
      size_t n_win = cseq.size() / W + 1;
      size_t n_blk = (n_perm > 0) ? (cseq.size() / B + 1) : 0;
      std::vector<double> win_pos(n_win, 0.0);
      std::vector<double> win_neg(n_win, 0.0);
      std::vector<uint64_t> win_cnt(n_win, 0);
      std::vector<Block> blocks(n_blk);
      uint64_t hits = 0;

      for (uint64_t pos = 0; pos < n_pos; ++pos) {
        const char* start = cseq.data() + pos;
        if (has_ambiguous(start, k)) continue;
        memcpy(t_fwd.data(), start, k);
        canonicalize(t_fwd.data(), t_canon.data(), t_rc.data(), k);

        uint64_t bulk1_cnt = t_bulk1_ra.query(t_canon.data());
        uint64_t bulk2_cnt = t_bulk2_ra.query(t_canon.data());

        uint64_t max_cnt = std::max(bulk1_cnt, bulk2_cnt);
        uint64_t total = bulk1_cnt + bulk2_cnt;
        if (max_cnt < min_total || total > max_total) continue;

        auto s = score_kmer(bulk2_cnt, bulk1_cnt,
                            params.bulk1_to_bulk2_scale,
                            params.min_depth, params.max_depth,
                            params.kai_min, params.g_min, params.error_rate);
        if (!s.passed) continue;
        ++hits;
        size_t mb = pos / W;
        auto [pos_e, neg_e] = directional_excess(s.z_score, tau);
        win_pos[mb] += pos_e;
        win_neg[mb] += neg_e;
        win_cnt[mb] += 1;
        if (n_perm > 0) {
          size_t bi = pos / B;
          blocks[bi].pos_ex += pos_e;
          blocks[bi].neg_ex += neg_e;
          blocks[bi].n += 1;
        }
      }

      auto& cr = results[ci];
      cr.n_hits = hits;
      for (size_t mb = 0; mb < n_win; ++mb) {
        if (win_cnt[mb] > 0)
          cr.peaks.push_back({cname, mb, win_pos[mb], win_neg[mb], win_cnt[mb], 1.0, 1.0});
      }
      cr.blocks = std::move(blocks);
    }

    t_bulk1_ra.close();
    t_bulk2_ra.close();
  }
}

int cmd_anchor(int argc, char** argv)
{
  auto opt = parse_anchor_args(argc, argv);

  fprintf(stderr, "[kbsa anchor] Auto-calculating parameters...\n");
  auto params = auto_params(opt.bulk1_hist, opt.bulk2_hist,
                            opt.kai_min, opt.g_min, opt.error_rate);

  fprintf(stderr, "[kbsa anchor] Parameters:\n");
  fprintf(stderr, "  scale = %.6f, min_depth = %lu, max_depth = %lu\n",
    params.bulk1_to_bulk2_scale,
    (unsigned long)params.min_depth, (unsigned long)params.max_depth);
  fprintf(stderr, "  kai_min = %.3f, g_min = %.3f\n", params.kai_min, params.g_min);
  fprintf(stderr, "  window = %u, step = %u\n", opt.window_size, opt.step_size);
  fprintf(stderr, "  min_density = %.3f, min_consistency = %.2f\n",
    opt.min_density, opt.min_consistency);

  fprintf(stderr, "[kbsa anchor] Opening KMC databases (random access)...\n");
  KmcRandomAccess bulk1_ra, bulk2_ra;
  if (!bulk1_ra.open(opt.bulk1_db))
    { fprintf(stderr, "ERROR: cannot open bulk1 db: %s\n", opt.bulk1_db.c_str()); return 1; }
  if (!bulk2_ra.open(opt.bulk2_db))
    { fprintf(stderr, "ERROR: cannot open bulk2 db: %s\n", opt.bulk2_db.c_str()); return 1; }

  const uint32_t k = bulk1_ra.kmer_length();
  fprintf(stderr, "[kbsa anchor] k = %u\n", k);

  // For peak mode: pre-read all chromosomes, then parallel process
  if (opt.peak_mode) {
    bulk1_ra.close();
    bulk2_ra.close();

    // Read all chromosomes
    FastaReader fasta(opt.reference);
    std::string chrom_name, chrom_seq;
    std::vector<std::pair<std::string, std::string>> chroms;
    while (fasta.next_chromosome(chrom_name, chrom_seq)) {
      if (!opt.target_chrom.empty() && chrom_name != opt.target_chrom) continue;
      if (chrom_seq.size() < k) continue;
      chroms.emplace_back(std::move(chrom_name), std::move(chrom_seq));
    }
    fprintf(stderr, "[kbsa anchor] Loaded %zu chromosomes for parallel peak scan\n", chroms.size());
    fprintf(stderr, "[kbsa anchor] Using %d threads (each opens own KMC RA, ~1.5GB index/thread)\n", opt.threads);
    if (opt.peak_tau > 0.0)
      fprintf(stderr, "[kbsa anchor] Excess-mass mode: tau=%.2f (score = sum(max(0, |z|-tau)))\n", opt.peak_tau);

    // Error/repeat filter derived from histogram (no empirical parameters)
    auto hist1_pk = load_histogram(opt.bulk1_hist);
    auto hist2_pk = load_histogram(opt.bulk2_hist);
    uint64_t valley1 = find_valley_depth(hist1_pk);
    uint64_t valley2 = find_valley_depth(hist2_pk);
    uint64_t peak1_v = opt.override_peak1 ? opt.override_peak1 : find_peak_depth(hist1_pk);
    uint64_t peak2_v = opt.override_peak2 ? opt.override_peak2 : find_peak_depth(hist2_pk);
    uint64_t min_total = std::max(valley1, valley2);
    uint64_t max_total = std::max(peak1_v, peak2_v) * kRepeatPeakMultiplier;
    fprintf(stderr, "[kbsa anchor] Peak1=%lu%s, Peak2=%lu%s, valley1=%lu, valley2=%lu\n",
            peak1_v, opt.override_peak1 ? "(override)" : "(heuristic)",
            peak2_v, opt.override_peak2 ? "(override)" : "(heuristic)",
            valley1, valley2);
    fprintf(stderr, "[kbsa anchor] Error/repeat filter: min_total=%lu, max_total=%lu\n",
            min_total, max_total);

    auto t0 = std::chrono::steady_clock::now();
    const uint32_t W = opt.peak_window;
    bool use_mean = (opt.peak_stat == "mean");
    const double tau = opt.peak_tau;

    std::vector<ChromResult> results(chroms.size());
    const uint32_t B = opt.perm_block;

    run_peak_scan(chroms, opt.bulk1_db, opt.bulk2_db, params,
                  k, W, B, tau, min_total, max_total,
                  opt.n_perm, opt.threads, results);

    // Merge peaks from per-chromosome results
    std::vector<Peak> all_peaks;
    uint64_t total_hits = 0;
    for (size_t ci = 0; ci < chroms.size(); ++ci) {
      total_hits += results[ci].n_hits;
      for (auto& p : results[ci].peaks)
        all_peaks.push_back(std::move(p));
      fprintf(stderr, "  %s: %lu hits\n",
        chroms[ci].first.c_str(), (unsigned long)results[ci].n_hits);
    }

    // ---- Optional: Block-permutation FDR ----
    if (opt.n_perm > 0)
      apply_permutation_fdr(all_peaks, results, chroms, opt.n_perm, W, B, opt.threads);

    // Sort: score = max(pos, neg). Coherence = score / (pos + neg).
    std::sort(all_peaks.begin(), all_peaks.end(),
      [use_mean](const Peak& a, const Peak& b) {
        double sa = std::max(a.pos_ex, a.neg_ex);
        double sb = std::max(b.pos_ex, b.neg_ex);
        if (use_mean) {
          sa /= std::max((uint64_t)1, a.n);
          sb /= std::max((uint64_t)1, b.n);
        }
        return sa > sb;
      });

    if (write_peak_bed(opt.output, all_peaks, W, use_mean, opt.n_perm > 0) != 0)
      return 1;

    auto dt = std::chrono::duration_cast<std::chrono::seconds>(
      std::chrono::steady_clock::now() - t0).count();
    fprintf(stderr, "\n[kbsa anchor] Peak mode done (parallel).\n");
    fprintf(stderr, "  Chromosomes: %zu\n", chroms.size());
    fprintf(stderr, "  Total hits: %lu\n", (unsigned long)total_hits);
    fprintf(stderr, "  Windows ranked: %zu (by %s)\n", all_peaks.size(), opt.peak_stat.c_str());
    fprintf(stderr, "  Time: %lds\n", (long)dt);
    fprintf(stderr, "  Output: %s\n", opt.output.c_str());

    // --- Orphan assembly ---
    if (opt.assemble_orphans) {
      fprintf(stderr, "\n[kbsa anchor] Assembling orphan k-mers (--assemble-orphans)...\n");

      Params unitig_params;
      unitig_params.bulk1_to_bulk2_scale = params.bulk1_to_bulk2_scale;
      unitig_params.error_rate = params.error_rate;

      auto diff_kmers = extract_differential_kmers(
          opt.bulk1_db, opt.bulk2_db, opt.bulk1_hist, opt.bulk2_hist,
          unitig_params, 0, 0);
      fprintf(stderr, "[kbsa anchor] Differential k-mers: %zu\n", diff_kmers.size());

      // Walk reference and mark k-mers that appear on it
      size_t n_anchored = 0;
      for (const auto& [cname, cseq] : chroms) {
        for (size_t pos = 0; pos + k <= cseq.size(); ++pos) {
          const char* start = cseq.data() + pos;
          if (has_ambiguous(start, k)) continue;
          uint64_t can = string_kmer_to_canonical(start, k);
          if (diff_kmers.erase(can)) ++n_anchored;
        }
      }
      fprintf(stderr, "[kbsa anchor] Anchored (removed): %zu, orphans remaining: %zu\n",
              n_anchored, diff_kmers.size());

      if (!diff_kmers.empty()) {
        auto orphan_unitigs = extract_unitigs(diff_kmers, k, opt.orphan_min_unitig);
        fprintf(stderr, "[kbsa anchor] Orphan unitigs assembled: %zu\n", orphan_unitigs.size());

        auto orphan_scores = score_and_rank_unitigs(
            orphan_unitigs, k, diff_kmers, unitig_params,
            static_cast<float>(opt.orphan_tau));

        // Write orphan FASTA
        std::string prefix = opt.output;
        auto dot = prefix.rfind('.');
        if (dot != std::string::npos) prefix = prefix.substr(0, dot);

        std::string ofa = prefix + "_orphan_unitigs.fa";
        std::ofstream ofa_out(ofa);
        for (const auto& s : orphan_scores) {
          ofa_out << ">" << s.id << " len=" << s.length
                  << " rank_score=" << s.rank_score << "\n"
                  << s.sequence << "\n";
        }
        ofa_out.close();

        std::string otsv = prefix + "_orphan_unitigs.tsv";
        std::ofstream otsv_out(otsv);
        otsv_out << "unitig_id\tlength\tn_kmers\tdominant_dir\tcoherence\tmean_z\t"
                    "excess_per_kb\tsum_excess_mass\tsum_bulk1\tsum_bulk2\n";
        for (const auto& s : orphan_scores) {
          const char* dir = (s.dominant_dir > 0) ? "BULK1" : "BULK2";
          char buf[512];
          int n = snprintf(buf, sizeof(buf),
            "%s\t%u\t%u\t%s\t%.4f\t%.4f\t%.4f\t%.4f\t%lu\t%lu\n",
            s.id.c_str(), s.length, s.n_kmers, dir,
            s.coherence, s.mean_z, s.excess_per_kb, s.sum_excess_mass,
            s.sum_bulk1, s.sum_bulk2);
          otsv_out.write(buf, n);
        }
        otsv_out.close();

        fprintf(stderr, "[kbsa anchor] Orphan output: %s (%zu unitigs)\n",
                ofa.c_str(), orphan_scores.size());
        fprintf(stderr, "[kbsa anchor] Orphan output: %s\n", otsv.c_str());
      } else {
        fprintf(stderr, "[kbsa anchor] No orphan k-mers — all differential k-mers are anchored.\n");
      }
    }

    return 0;
  }

  // --- Non-peak modes (per-position / legacy window) below ---

  std::ofstream out(opt.output);
  if (!out.good())
    { fprintf(stderr, "ERROR: cannot open output: %s\n", opt.output.c_str()); return 1; }

  if (opt.peak_mode)
    out << "#chrom\tstart\tend\tn_kmers\tsum_abs_z\tmean_abs_z\tpeak_score\n";
  else if (opt.per_position)
    out << "#chrom\tpos\tbulk1_raw\tbulk2_raw\tz_score\n";
  else
    out << "#chrom\tstart\tend\thits\twindow_kmers\tdensity\tconsistency"
        << "\tmean_kai_dev\tmax_g\n";

  // Per-chromosome peak data (only used in --peak mode):
  //   key = (chrom_name, mb_index), value = (sum_abs_z, count)
  std::vector<std::tuple<std::string, uint64_t, double, uint64_t>> all_peaks;

  auto t0 = std::chrono::steady_clock::now();
  uint64_t total_intervals = 0;
  uint64_t total_chroms = 0;

  FastaReader fasta(opt.reference);
  std::string chrom_name, chrom_seq;

  std::vector<char> fwd_buf(k + 1, '\0');
  std::vector<char> rc_buf(k + 1, '\0');
  std::vector<char> canon_buf(k + 1, '\0');

  while (fasta.next_chromosome(chrom_name, chrom_seq)) {
    if (!opt.target_chrom.empty() && chrom_name != opt.target_chrom)
      continue;

    if (chrom_seq.size() < k) continue;
    ++total_chroms;

    const uint64_t n_positions = chrom_seq.size() - k + 1;
    fprintf(stderr, "[kbsa anchor] %s: %lu bp, %lu k-mer positions\n",
      chrom_name.c_str(), (unsigned long)chrom_seq.size(), (unsigned long)n_positions);

    if (opt.peak_mode || opt.per_position) {
      // Per-position scan: for peak mode, aggregate into Mb windows on the fly.
      // one-sided filter: skip only if both bulks are zero
      uint64_t n_hits = 0;
      char buf[256];

      // Peak mode: per-Mb window accumulators for THIS chromosome
      std::vector<double> win_sum_abs_z;
      std::vector<uint64_t> win_count;
      const uint32_t W = opt.peak_window;
      if (opt.peak_mode) {
        size_t n_win = chrom_seq.size() / W + 1;
        win_sum_abs_z.assign(n_win, 0.0);
        win_count.assign(n_win, 0);
      }

      for (uint64_t pos = 0; pos < n_positions; ++pos) {
        const char* start = chrom_seq.data() + pos;
        if (has_ambiguous(start, k)) continue;

        memcpy(fwd_buf.data(), start, k);
        fwd_buf[k] = '\0';
        canonicalize(fwd_buf.data(), canon_buf.data(), rc_buf.data(), k);

        uint64_t bulk1_cnt = bulk1_ra.query(canon_buf.data());
        uint64_t bulk2_cnt = bulk2_ra.query(canon_buf.data());

        // shared-only filter for peak mode (BSA proper signal)
        // per-position mode: emit any non-zero
        if (opt.peak_mode) {
          if (opt.shared_only) {
            if (bulk1_cnt == 0 || bulk2_cnt == 0) continue;
          } else {
            if (bulk1_cnt == 0 && bulk2_cnt == 0) continue;
          }
        } else {
          if (bulk1_cnt == 0 && bulk2_cnt == 0) continue;
        }

        auto s = score_kmer(bulk2_cnt, bulk1_cnt,
                            params.bulk1_to_bulk2_scale,
                            params.min_depth, params.max_depth,
                            params.kai_min, params.g_min, params.error_rate);

        if (!s.passed) continue;
        ++n_hits;

        if (opt.peak_mode) {
          size_t mb = pos / W;
          if (mb < win_sum_abs_z.size()) {
            double excess = std::abs(s.z_score) - opt.peak_tau;
            if (excess > 0.0)
              win_sum_abs_z[mb] += excess;
            win_count[mb] += 1;
          }
        } else {
          int n = snprintf(buf, sizeof(buf), "%s\t%lu\t%lu\t%lu\t%+.4f\n",
            chrom_name.c_str(), (unsigned long)pos,
            (unsigned long)bulk1_cnt, (unsigned long)bulk2_cnt, s.z_score);
          out.write(buf, n);
        }

        if (pos % 5000000 == 0 && pos > 0) {
          auto dt = std::chrono::duration_cast<std::chrono::seconds>(
            std::chrono::steady_clock::now() - t0).count();
          fprintf(stderr, "  %luM positions, %lu hits (%lds)\n",
            (unsigned long)(pos/1000000), (unsigned long)n_hits, (long)dt);
        }
      }

      // Stash peak windows for global ranking
      if (opt.peak_mode) {
        for (size_t mb = 0; mb < win_sum_abs_z.size(); ++mb) {
          if (win_count[mb] > 0) {
            all_peaks.emplace_back(chrom_name, mb, win_sum_abs_z[mb], win_count[mb]);
          }
        }
      }

      fprintf(stderr, "  %lu hits from %lu positions (%.2f%%)\n",
        (unsigned long)n_hits, (unsigned long)n_positions,
        n_positions > 0 ? 100.0 * n_hits / n_positions : 0.0);
      total_intervals += n_hits;
      continue;  // skip legacy window aggregation
    }

    // Window mode (legacy)
    std::vector<AnchorHit> hits;
    hits.reserve(n_positions / 10);

    for (uint64_t pos = 0; pos < n_positions; ++pos) {
      const char* start = chrom_seq.data() + pos;
      if (has_ambiguous(start, k)) continue;

      memcpy(fwd_buf.data(), start, k);
      fwd_buf[k] = '\0';
      canonicalize(fwd_buf.data(), canon_buf.data(), rc_buf.data(), k);

      uint64_t bulk1_cnt = bulk1_ra.query(canon_buf.data());
      uint64_t bulk2_cnt = bulk2_ra.query(canon_buf.data());

      auto s = score_kmer(bulk2_cnt, bulk1_cnt,
                          params.bulk1_to_bulk2_scale,
                          params.min_depth, params.max_depth,
                          params.kai_min, params.g_min, params.error_rate);

      if (s.passed) {
        hits.push_back({pos, s.kai_reg, s.g_score, s.sign});
      }
    }

    fprintf(stderr, "  %lu hits from %lu positions (%.2f%%)\n",
      (unsigned long)hits.size(), (unsigned long)n_positions,
      n_positions > 0 ? 100.0 * hits.size() / n_positions : 0.0);

    if (hits.empty()) continue;

    for (uint64_t win_start = 0;
         win_start + opt.window_size <= chrom_seq.size();
         win_start += opt.step_size)
    {
      uint64_t win_end = win_start + opt.window_size;
      uint32_t window_kmers = opt.window_size - k + 1;

      auto it_lo = std::lower_bound(hits.begin(), hits.end(), win_start,
        [](const AnchorHit& h, uint64_t v) { return h.position < v; });
      auto it_hi = std::lower_bound(hits.begin(), hits.end(), win_end,
        [](const AnchorHit& h, uint64_t v) { return h.position < v; });

      uint32_t hit_count = static_cast<uint32_t>(it_hi - it_lo);
      if (hit_count == 0) continue;

      double density = static_cast<double>(hit_count) / window_kmers;
      if (density < opt.min_density) continue;

      uint32_t bulk2_dir = 0, bulk1_dir = 0;
      double kai_dev_sum = 0.0;
      double max_g = 0.0;
      for (auto it = it_lo; it != it_hi; ++it) {
        if (it->direction == Significance::BULK2) ++bulk2_dir;
        else ++bulk1_dir;
        kai_dev_sum += std::abs(it->kai_reg - 0.5);
        if (it->g_score > max_g) max_g = it->g_score;
      }

      double consistency = static_cast<double>(std::max(bulk2_dir, bulk1_dir)) / hit_count;
      if (consistency < opt.min_consistency) continue;

      double mean_kai_dev = kai_dev_sum / hit_count;

      char buf[512];
      int n = snprintf(buf, sizeof(buf),
        "%s\t%lu\t%lu\t%u\t%u\t%.4f\t%.3f\t%.4f\t%.2f\n",
        chrom_name.c_str(),
        (unsigned long)win_start, (unsigned long)win_end,
        hit_count, window_kmers, density, consistency,
        mean_kai_dev, max_g);
      out.write(buf, n);
      ++total_intervals;
    }
  }

  // Peak mode: sort all windows globally and output ranked BED
  if (opt.peak_mode && !all_peaks.empty()) {
    bool use_mean = (opt.peak_stat == "mean");
    // Sort by chosen statistic descending
    std::sort(all_peaks.begin(), all_peaks.end(),
      [use_mean](const auto& a, const auto& b) {
        double sa = use_mean ? std::get<2>(a) / std::max((uint64_t)1, std::get<3>(a))
                             : std::get<2>(a);
        double sb = use_mean ? std::get<2>(b) / std::max((uint64_t)1, std::get<3>(b))
                             : std::get<2>(b);
        return sa > sb;
      });

    char buf[512];
    const uint32_t W = opt.peak_window;
    for (const auto& [chrom, mb, sum_abs, cnt] : all_peaks) {
      double mean_abs = cnt > 0 ? sum_abs / cnt : 0.0;
      double score = use_mean ? mean_abs : sum_abs;
      uint64_t start = mb * W;
      uint64_t end = start + W;
      int n = snprintf(buf, sizeof(buf), "%s\t%lu\t%lu\t%lu\t%.4f\t%.6f\t%.4f\n",
        chrom.c_str(), (unsigned long)start, (unsigned long)end,
        (unsigned long)cnt, sum_abs, mean_abs, score);
      out.write(buf, n);
    }
    fprintf(stderr, "[kbsa anchor] Peak mode: %zu windows ranked by %s\n",
      all_peaks.size(), opt.peak_stat.c_str());
  }

  bulk1_ra.close();
  bulk2_ra.close();

  auto dt = std::chrono::duration_cast<std::chrono::seconds>(
    std::chrono::steady_clock::now() - t0).count();

  fprintf(stderr, "\n[kbsa anchor] Done.\n");
  fprintf(stderr, "  Chromosomes: %lu\n", (unsigned long)total_chroms);
  fprintf(stderr, "  Candidate intervals: %lu\n", (unsigned long)total_intervals);
  fprintf(stderr, "  Time: %lds\n", (long)dt);
  fprintf(stderr, "  Output: %s\n", opt.output.c_str());
  return 0;
}

struct RankOptions
{
  std::string score_tsv;
  std::string unitig_fa;
  std::string output;
  double min_g {0.0};
  uint32_t min_kmers_scored {1};
  bool include_seq {false};
};

void print_rank_usage(const char* prog)
{
  fprintf(stderr,
    "kbsa rank - aggregate per-k-mer scores onto BCALM2 unitigs\n\n"
    "Usage: %s rank --score <scored.tsv> --unitigs <unitigs.fa> -o <ranked.tsv> [opts]\n\n"
    "Options:\n"
    "  --min-g <float>    Pre-filter k-mers below g_score (default: 0.0)\n"
    "  --min-kmers <int>  Skip unitigs with fewer scored k-mers (default: 1)\n"
    "  --emit-seq         Include unitig sequence column\n\n"
    "Output ranked by sum_abs_z desc.\n", prog);
}

int cmd_rank(int argc, char** argv)
{
  RankOptions opt;
  for (int i = 2; i < argc; ++i) {
    if (strcmp(argv[i], "--score") == 0 && i+1 < argc) opt.score_tsv = argv[++i];
    else if (strcmp(argv[i], "--unitigs") == 0 && i+1 < argc) opt.unitig_fa = argv[++i];
    else if (strcmp(argv[i], "-o") == 0 && i+1 < argc) opt.output = argv[++i];
    else if (strcmp(argv[i], "--min-g") == 0 && i+1 < argc) opt.min_g = atof(argv[++i]);
    else if (strcmp(argv[i], "--min-kmers") == 0 && i+1 < argc) opt.min_kmers_scored = atoi(argv[++i]);
    else if (strcmp(argv[i], "--emit-seq") == 0) opt.include_seq = true;
    else if (strcmp(argv[i], "-h") == 0 || strcmp(argv[i], "--help") == 0) {
      print_rank_usage(argv[0]); return 0;
    } else { fprintf(stderr, "Unknown arg: %s\n", argv[i]); return 1; }
  }
  if (opt.score_tsv.empty() || opt.unitig_fa.empty() || opt.output.empty()) {
    print_rank_usage(argv[0]); return 1;
  }

  fprintf(stderr, "[rank] loading score table from %s ...\n", opt.score_tsv.c_str());
  std::unordered_map<uint64_t, KmerEntry> tbl;
  size_t n_loaded = load_score_table(opt.score_tsv, opt.min_g, tbl);
  if (n_loaded == 0) return 1;

  fprintf(stderr, "[rank] aggregating unitigs from %s ...\n", opt.unitig_fa.c_str());
  std::ifstream fa(opt.unitig_fa);
  if (!fa.good()) { fprintf(stderr, "ERROR: open %s\n", opt.unitig_fa.c_str()); return 1; }

  std::vector<UnitigStats> rows;
  std::string line, header, seq;
  auto flush = [&]() {
    if (header.empty() || seq.empty()) return;
    auto u = aggregate_unitig(header, seq, tbl);
    if (u.n_kmers_scored >= opt.min_kmers_scored) rows.push_back(std::move(u));
  };
  while (std::getline(fa, line)) {
    if (line.empty()) continue;
    if (line[0] == '>') {
      flush();
      size_t sp = line.find(' ');
      header = (sp == std::string::npos) ? line.substr(1) : line.substr(1, sp - 1);
      seq.clear();
    } else {
      seq.append(line);
    }
  }
  flush();

  fprintf(stderr, "[rank] %zu unitigs aggregated; sorting by sum_abs_z ...\n", rows.size());
  std::sort(rows.begin(), rows.end(),
    [](const UnitigStats& a, const UnitigStats& b) { return a.sum_abs_z > b.sum_abs_z; });

  std::ofstream out(opt.output);
  if (!out.good()) { fprintf(stderr, "ERROR: open %s\n", opt.output.c_str()); return 1; }
  out << "rank\tunitig_id\tlength\tn_kmers\tn_scored\tn_bulk1\tn_bulk2\t"
         "sum_abs_z\tmedian_abs_z\tmax_abs_z\tsigned_sum_z\tdominant_dir";
  if (opt.include_seq) out << "\tsequence";
  out << "\n";

  char buf[1024];
  for (size_t i = 0; i < rows.size(); ++i) {
    const auto& u = rows[i];
    const char* dir = (u.n_bulk2 > u.n_bulk1) ? "BULK2" :
                      (u.n_bulk1 > u.n_bulk2) ? "BULK1" : "MIXED";
    int n = snprintf(buf, sizeof(buf),
      "%zu\t%s\t%u\t%u\t%u\t%u\t%u\t%.4f\t%.4f\t%.4f\t%+.4f\t%s",
      i + 1, u.id.c_str(), u.length, u.n_kmers_total, u.n_kmers_scored,
      u.n_bulk1, u.n_bulk2, u.sum_abs_z, u.median_abs_z, u.max_abs_z,
      u.sum_signed_z, dir);
    out.write(buf, n);
    if (opt.include_seq) { out.put('\t'); out.write(u.sequence.data(), u.sequence.size()); }
    out.put('\n');
  }
  fprintf(stderr, "[rank] wrote %zu ranked unitigs to %s\n", rows.size(), opt.output.c_str());
  return 0;
}

