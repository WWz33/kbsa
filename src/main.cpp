int cmd_score(int argc, char** argv)
{
  auto opt = parse_args(argc, argv);

  fprintf(stderr, "[kbsa] Auto-calculating parameters from histograms...\n");
  auto params = auto_params(opt.bulk1_hist, opt.bulk2_hist,
                            opt.kai_min, opt.g_min, opt.error_rate);

  fprintf(stderr, "[kbsa] Parameters:\n");
  fprintf(stderr, "  scale_case_to_control = %.6f\n", params.bulk1_to_bulk2_scale);
  fprintf(stderr, "  min_depth = %lu\n", (unsigned long)params.min_depth);
  fprintf(stderr, "  max_depth = %lu\n", (unsigned long)params.max_depth);
  fprintf(stderr, "  kai_min = %.3f\n", params.kai_min);
  fprintf(stderr, "  g_min = %.3f\n", params.g_min);
  fprintf(stderr, "  error_rate = %.4f\n", params.error_rate);

  fprintf(stderr, "[kbsa] Opening KMC databases...\n");
  KmcMergeIterator merger(opt.bulk1_db, opt.bulk2_db);
  fprintf(stderr, "[kbsa] k = %u\n", merger.kmer_length());

  FILE* out = std::fopen(opt.output.c_str(), "wb");
  if (!out) {
    fprintf(stderr, "ERROR: cannot open output file: %s\n", opt.output.c_str());
    return 1;
  }
  static char io_buf[1 << 22];  // 4MB stdio buffer
  std::setvbuf(out, io_buf, _IOFBF, sizeof(io_buf));

  std::fputs("kmer\tsignificance\tkai_reg\tg_score\tz_score\tbulk2_raw\tbulk1_raw\tbulk2_adj\tbulk1_adj\n", out);

  auto t0 = std::chrono::steady_clock::now();
  uint64_t total = 0, passed = 0, bulk2_sig = 0, bulk1_sig = 0;
  char buf[512];

  MergedKmer mk;
  while (merger.next(mk)) {
    ++total;
    auto s = score_kmer(mk.bulk2_count, mk.bulk1_count,
                        params.bulk1_to_bulk2_scale,
                        params.min_depth, params.max_depth,
                        params.kai_min, params.g_min, params.error_rate);

    if (s.passed) {
      ++passed;
      if (s.sign == Significance::BULK2) ++bulk2_sig;
      else if (s.sign == Significance::BULK1) ++bulk1_sig;

      int n = snprintf(buf, sizeof(buf),
        "%s\t%s\t%.6f\t%.4f\t%+.4f\t%lu\t%lu\t%.4f\t%.4f\n",
        mk.kmer_str,
        significance_str(s.sign),
        s.kai_reg, s.g_score, s.z_score,
        (unsigned long)s.cnt_bulk2_raw, (unsigned long)s.cnt_bulk1_raw,
        s.cnt_bulk2_adj, s.cnt_bulk1_adj);
      std::fwrite(buf, 1, n, out);
    }

    if (total % 50000000 == 0) {
      auto dt = std::chrono::duration_cast<std::chrono::seconds>(
        std::chrono::steady_clock::now() - t0).count();
      fprintf(stderr, "[kbsa] %luM kmers processed, %luM passed (%lds)\n",
        (unsigned long)(total/1000000), (unsigned long)(passed/1000000), (long)dt);
    }
  }

  std::fclose(out);

  auto dt = std::chrono::duration_cast<std::chrono::seconds>(
    std::chrono::steady_clock::now() - t0).count();

  fprintf(stderr, "\n[kbsa] Done.\n");
  fprintf(stderr, "  Total kmers: %lu\n", (unsigned long)total);
  fprintf(stderr, "  Passed: %lu (%.2f%%)\n", (unsigned long)passed,
    total > 0 ? 100.0 * passed / total : 0.0);
  fprintf(stderr, "  BULK2-enriched: %lu\n", (unsigned long)bulk2_sig);
  fprintf(stderr, "  BULK1-enriched: %lu\n", (unsigned long)bulk1_sig);
  fprintf(stderr, "  Time: %lds\n", (long)dt);
  fprintf(stderr, "  Output: %s\n", opt.output.c_str());

  return 0;
}

