CountOptions parse_count_args(int argc, char** argv)
{
  CountOptions opt;
  enum { NONE, BULK1, BULK2 } mode = NONE;

  for (int i = 2; i < argc; ++i) {
    if (strcmp(argv[i], "--bulk1") == 0) { mode = BULK1; continue; }
    if (strcmp(argv[i], "--bulk2") == 0) { mode = BULK2; continue; }
    if (strcmp(argv[i], "--only-bulk1") == 0) { opt.only_bulk1 = true; mode = NONE; continue; }
    if (strcmp(argv[i], "--only-bulk2") == 0) { opt.only_bulk2 = true; mode = NONE; continue; }
    if (strcmp(argv[i], "-o") == 0 && i+1 < argc) { opt.output_dir = argv[++i]; mode = NONE; continue; }
    if (strcmp(argv[i], "-k") == 0 && i+1 < argc) { opt.k = atoi(argv[++i]); mode = NONE; continue; }
    if (strcmp(argv[i], "-t") == 0 && i+1 < argc) { opt.threads = atoi(argv[++i]); mode = NONE; continue; }
    if (strcmp(argv[i], "-m") == 0 && i+1 < argc) { opt.mem_gb = atoi(argv[++i]); mode = NONE; continue; }
    if (strcmp(argv[i], "--ci") == 0 && i+1 < argc) { opt.ci = atoll(argv[++i]); mode = NONE; continue; }
    if (strcmp(argv[i], "--cs") == 0 && i+1 < argc) { opt.cs = atoll(argv[++i]); mode = NONE; continue; }

    if (mode == BULK1) opt.bulk1_files.push_back(argv[i]);
    else if (mode == BULK2) opt.bulk2_files.push_back(argv[i]);
  }

  if (opt.output_dir.empty()) {
    print_count_usage(argv[0]);
    exit(1);
  }
  if (!opt.only_bulk2 && opt.bulk1_files.empty()) {
    fprintf(stderr, "ERROR: --bulk1 files required\n");
    exit(1);
  }
  if (!opt.only_bulk1 && opt.bulk2_files.empty()) {
    fprintf(stderr, "ERROR: --bulk2 files required\n");
    exit(1);
  }
  return opt;
}

bool run_kmc_count(const std::vector<std::string>& input_files,
                   const std::string& output_db,
                   const std::string& tmp_dir,
                   uint32_t k, uint32_t threads, uint32_t mem_gb,
                   uint64_t ci, uint64_t cs)
{
  fs::create_directories(tmp_dir);

  KMC::Runner runner;

  KMC::Stage1Params s1;
  s1.SetInputFiles(input_files)
    .SetKmerLen(k)
    .SetNThreads(threads)
    .SetMaxRamGB(mem_gb)
    .SetTmpPath(tmp_dir)
    .SetInputFileType(KMC::InputFileType::FASTQ)
    .SetCanonicalKmers(true);

  auto r1 = runner.RunStage1(s1);
  fprintf(stderr, "  Stage1: %.1fs, %lu sequences\n", r1.time, (unsigned long)r1.nSeqences);

  KMC::Stage2Params s2;
  s2.SetCutoffMin(ci)
    .SetCounterMax(cs)
    .SetNThreads(threads)
    .SetMaxRamGB(mem_gb)
    .SetOutputFileName(output_db);

  auto r2 = runner.RunStage2(s2);
  fprintf(stderr, "  Stage2: %.1fs, %lu unique k-mers\n", r2.time, (unsigned long)r2.nUniqueKmers);

  fs::remove_all(tmp_dir);
  return true;
}

void generate_histogram(const std::string& db_path, const std::string& hist_path)
{
  CKMCFile db;
  if (!db.OpenForListing(db_path)) {
    fprintf(stderr, "ERROR: cannot open %s for histogram\n", db_path.c_str());
    return;
  }

  CKMCFileInfo info;
  db.Info(info);

  std::vector<uint64_t> hist(info.max_count + 1, 0);
  CKmerAPI kmer(info.kmer_length);
  uint32 count;
  while (db.ReadNextKmer(kmer, count)) {
    if (count <= info.max_count) hist[count]++;
  }
  db.Close();

  std::ofstream out(hist_path);
  for (uint64_t i = 1; i < hist.size(); ++i) {
    if (hist[i] > 0) out << i << "\t" << hist[i] << "\n";
  }
}

int cmd_count(int argc, char** argv)
{
  auto opt = parse_count_args(argc, argv);

  fs::create_directories(opt.output_dir);
  std::string bulk1_db = opt.output_dir + "/bulk1_db";
  std::string bulk2_db = opt.output_dir + "/bulk2_db";
  std::string bulk1_tmp = opt.output_dir + "/tmp_bulk1";
  std::string bulk2_tmp = opt.output_dir + "/tmp_bulk2";

  fprintf(stderr, "[kbsa count] k=%u, ci=%lu, threads=%u, mem=%uGB\n",
    opt.k, (unsigned long)opt.ci, opt.threads, opt.mem_gb);
  fprintf(stderr, "[kbsa count] Bulk1 files: %zu, Bulk2 files: %zu\n",
    opt.bulk1_files.size(), opt.bulk2_files.size());
  fprintf(stderr, "[kbsa count] Output: %s\n\n", opt.output_dir.c_str());

  auto t0 = std::chrono::steady_clock::now();

  if (!opt.only_bulk2) {
    fprintf(stderr, "[kbsa count] Counting bulk1 pool...\n");
    run_kmc_count(opt.bulk1_files, bulk1_db, bulk1_tmp,
                  opt.k, opt.threads, opt.mem_gb, opt.ci, opt.cs);
  }

  if (!opt.only_bulk1) {
    fprintf(stderr, "[kbsa count] Counting bulk2 pool...\n");
    run_kmc_count(opt.bulk2_files, bulk2_db, bulk2_tmp,
                  opt.k, opt.threads, opt.mem_gb, opt.ci, opt.cs);
  }

  fprintf(stderr, "\n[kbsa count] Generating histograms...\n");
  if (!opt.only_bulk2)
    generate_histogram(bulk1_db, opt.output_dir + "/bulk1_hist.txt");
  if (!opt.only_bulk1)
    generate_histogram(bulk2_db, opt.output_dir + "/bulk2_hist.txt");

  auto dt = std::chrono::duration_cast<std::chrono::seconds>(
    std::chrono::steady_clock::now() - t0).count();

  fprintf(stderr, "\n[kbsa count] Done in %lds.\n", (long)dt);
  fprintf(stderr, "  Bulk1 DB: %s\n", bulk1_db.c_str());
  fprintf(stderr, "  Bulk2 DB: %s\n", bulk2_db.c_str());
  fprintf(stderr, "  Histograms: %s/bulk1_hist.txt, bulk2_hist.txt\n", opt.output_dir.c_str());
  return 0;
}

